    <h1>Międzynarodowy projekt Erasmus Plus</h1>
    <div id="main-text">
        <div style="flex: 3 1 0px; display: flex; flex-direction: column">
            <p>W roku szkolnym 2021/22 szkoła otrzymała na lata <strong>2021-2027</strong> akredytację w programie <strong>Erasmus+</strong> Kształcenie i szkolenia zawodowe Akcja 1 Mobilność edukacyjna.</p>           
            <blockquote><strong>„Akredytacja”</strong> to możliwość uczestnictwa w programie, to rodzaj „biletu wstępu do programu” i gwarancja regularnego otrzymywania dofinansowania na działania związane z mobilnością w ramach programu Erasmus.</blockquote> 
            <p>W programie na lata <strong>2021–2027</strong> kładzie się silny nacisk na włączenie społeczne, transformację ekologiczną i cyfrową oraz promowanie uczestnictwa młodych ludzi w życiu. </p>
            <p>Program wspiera realizację priorytetów i działań określonych w europejskim obszarze edukacji, Planie działania w dziedzinie edukacji cyfrowej oraz europejskim programie na rzecz umiejętności. Program ten również:</p>
            <ul>
                <li>wspiera Europejski filar praw socjalnych</li>
                <li>służy wdrożeniu strategii UE na rzecz młodzieży na lata 2019–2027</li>
                <li>pozwala rozwijać europejski wymiar sport</li>
            </ul>
            <p><b>I DZIAŁANIE</b> - 30 czerwiec 2022 – 30 czerwca 2023 Kształcenie i szkolenia zawodowe Akcja1 Mobilność edukacyjna <b>nr 2022-1-PL01-KA121-VET-000059553</b></p>
            <h2>Szczególne cele</h2>
            <ol>
                <li>Podniesienie jakości podstawowego i ustawicznego kształcenia i szkolenia zawodowego (IVET i CVET) w Europie</li>
                <li>Wzmocnienie europejskiego wymiaru nauczania i uczenia.</li>
            </ol>
            <h2>Klasy uczestniczące w projekcie</h2>
            <span> <strong>2TP</strong> – technik programista <br></span>
            <span><strong>2TGs</strong> – technik żywienia i usług gastronomicznych</span>
            <h2>Partnerzy</h2>
